void main() {
  // Step 1: Initialize Variables
  int number = 15;

  // Step 2: Check Even or Odd
  if (number % 2 == 0) {
    print('$number is even.');
  } else {
    print('$number is odd.');
  }

  // Step 3: Print Numbers from 1 to 5
  for (int i = 1; i <= 5; i++) {
    print('Number: $i');
  }
}
